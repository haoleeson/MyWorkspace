#!/bin/bash

# 常用的ANSI控制码如下（有些不支持）：
#    - 10-19 关于字体的
#    - 21-29 基本与1-9正好相反
#    - 30-37 设置前景色
#    - 40-47 设置背景色
#    - 50-89 没用
#    - 90-109 又是设置前景背景的，比之前的颜色浅

func_show_shell_control() {
    # 关闭所有属性 
    echo "\033[0m 关闭所有属性 \033[0m"
    # 高亮
    echo "\033[1m 高亮 \033[0m"
    # 亮度减半
    echo "\033[2m 亮度减半 \033[0m"
    # 斜体
    echo "\033[3m 斜体 \033[0m"
    # 下划线 
    echo "\033[4m 下划线  \033[0m"
    # 闪烁 
    echo "\033[5m 闪烁  \033[0m"
    # 快闪
    echo "\033[6m 快闪 \033[0m"
    # 反显 
    echo "\033[7m 反显  \033[0m"
    # 消隐 
    echo "\033[8m 消隐  \033[0m"
    # 中间一道横线
    echo "\033[9m 中间一道横线 \033[0m"
}

func_show_front_color() {
    # 30:黑
    echo "\033[30m black \033[39m"
    # 31:红
    echo "\033[31m red \033[39m"
    # 32:绿
    echo "\033[32m green \033[39m"
    # 33:黄
    echo "\033[33m yellow \033[39m"
    # 34:蓝色
    echo "\033[34m blue \033[39m"
    # 35:紫色
    echo "\033[35m Purple \033[39m"
    # 36:深绿
    echo "\033[36m light blue \033[39m"
    # 37:白色
    echo "\033[37m white \033[39m"
    # 38 打开下划线,设置默认前景色 
    echo "\033[38m 打开下划线,设置默认前景色 \033[39m"
    # 39 关闭下划线,设置默认前景色 
    echo "\033[39m 关闭下划线,设置默认前景色 \033[39m"
}

func_show_background_color() {
    # 40 黑色背景
    echo "\033[40m 黑色背景 \033[49m"
    # 41 红色背景 
    echo "\033[41m 红色背景 \033[49m"
    # 42 绿色背景 
    echo "\033[42m 绿色背景 \033[49m"
    # 43 棕色背景 
    echo "\033[43m 棕色背景 \033[49m"
    # 44 蓝色背景 
    echo "\033[44m 蓝色背景 \033[49m"
    # 45 品红背景 
    echo "\033[45m 品红背景 \033[49m"
    # 46 孔雀蓝背景 
    echo "\033[46m 孔雀蓝背景 \033[49m"
    # 47 白色背景 
    echo "\033[47m 白色背景 \033[49m"
    # 48 不知道什么东西
    echo "\033[48m 不知道什么东西 \033[49m"
    # 49 设置默认背景色
    echo "\033[49m 设置默认背景色 \033[49m"
}

# \033[nA 光标上移n行 
# \033[nB 光标下移n行 
# \033[nC 光标右移n行 
# \033[nD 光标左移n行 
# \033[y;xH设置光标位置 
# \033[2J 清屏 
# \033[K 清除从光标到行尾的内容 
# \033[s 保存光标位置 
# \033[u 恢复光标位置 
# \033[?25l 隐藏光标 
# \033[?25h 显示光标


func_show_shell_control

func_show_front_color

func_show_front_color
